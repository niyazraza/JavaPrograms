package com.niyaz.method_overloading_26august;

public class DataTypePrinter
{
   public void printData(byte n) 
   {
	   System.out.println("byte data type is: "+n);
   }
   public void printData(short n) 
   {
	   System.out.println("short data type is: "+n);   
   }
   public void printData(int n) 
   {
	   System.out.println("int data type is: "+n);
   }
   public void printData(long m) 
   {
	   System.out.println("long data type is: "+m);
   }
   public void printData(float n) 
   {
	   System.out.println("float data type is: "+n);
	   
   }
   public void printData(double n) 
   {
	   System.out.println("double data type is: "+n);
   }
   public void printData(char n) 
   {
	   System.out.println("char data type is: "+n);
   }
   public void printData(boolean n) 
   {
	   System.out.println("boolean data type is: "+n);
	   
   }
   public void printData(String n) 
   {
	   System.out.println("String data type is: "+n);
   }
}
